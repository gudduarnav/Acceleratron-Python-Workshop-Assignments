# Write a program to add 8 to the number 2345 and then divide it by 3.
# Now, the modulus of the quotient is taken with 5 and
# then multiply the resultant value by 5.
# Display the result.

number = 2345
print(number)

number = number + 8
print(number)

number = number / 3
print(number)

number = number % 5
print(number)

number = number * 5
print(number)
